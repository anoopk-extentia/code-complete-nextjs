//@ts-nocheck

import type { NextPage } from "next";
import Head from "next/head";
import { useEffect } from "react";
import styles from "../styles/Home.module.css";
import { useActions } from "hooks/useActions";
import { useTypedSelector } from "hooks/useTypedSelector";
import { CCwidget, CCwidget2 } from "components";

const Home: NextPage = () => {
  const { getWidgetList } = useActions();
  useEffect(() => {
    getWidgetList({ subscriberId: 1 });
  }, []);

  const { isError, isLoading, data } = useTypedSelector(
    ({ widgetList }) => widgetList
  );

  if (isLoading) return <>Loading...</>;
  if (isError) return <>Something went wrong !!</>;

  const getWidgets = (widgetId: number, subscriberId: number) => {
    switch (widgetId) {
      case 1:
        return (
          <CCwidget
            key={widgetId}
            widgetId={widgetId}
            subscriberId={subscriberId}
          />
        );
      case 2:
        return (
          <CCwidget2
            key={widgetId}
            widgetId={widgetId}
            subscriberId={subscriberId}
          />
        );
      default:
        return;
    }
  };
  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <div className="grid grid-cols-3 gap-2">
        {data.map(({ widget_id }, i) => getWidgets(widget_id, 1))}
      </div>
    </div>
  );
};

export default Home;
